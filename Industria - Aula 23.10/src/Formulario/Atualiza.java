    private void btAtualizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizaActionPerformed
      if(rdProduto.isSelected()){
        Produto p = new Produto();
        p.setId(Integer.parseInt(txtId.getText()));
        p.setNome(txtNomeP.getText());
        p.setPreco(Double.parseDouble(txtPreco.getText()));
        p.setQnt(Integer.parseInt(txtQnt.getText()));
        p.setId_categoria((Categoria) cbCategoria.getSelectedItem());

        ProdutoDAO pDAO = new ProdutoDAO();
        pDAO.editar(p);
        limparFormulario();
      }
      else if(rdCategoria.isSelected()){
        Categoria c = new Categoria();
        c.setId(Integer.parseInt(txtProduto.getText()));
        c.setNome(txtCategoria.getText());

        CategoriaDAO cDAO = new CategoriaDAO();
        cDAO.editar(c);
        limparFormulario();
      }
      else{
          JOptionPane.showConfirmDialog(null, "Selecione um Classe", "Ok", JOptionPane.YES_NO_OPTION);
      }
    }//GEN-LAST:event_btAtualizaActionPerformed

    private void btExcluiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluiActionPerformed
     int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclus√£o", JOptionPane.YES_NO_OPTION);
    
     if(rdProduto.isSelected()){
        if(resposta == JOptionPane.YES_OPTION){
            ProdutoDAO pDAO = new ProdutoDAO();
            pDAO.excluir(Integer.parseInt(txtProduto.getText()));
            limparFormulario();
        }
    }
     else if(rdCategoria.isSelected()){
        if(resposta == JOptionPane.YES_OPTION){
            CategoriaDAO cDAO = new CategoriaDAO();
            cDAO.excluir(Integer.parseInt(txtId.getText()));
            limparFormulario();
        }
         
     }
    }//GEN-LAST:event_btExcluiActionPerformed

     public void preencherComboCategorias() {
        CategoriaDAO cDAO = new CategoriaDAO();
        List<Categoria> listac = cDAO.getCategorias();
        for (Categoria c : listac) {
        cbCategoria.addItem(c);
       }
    }
    
     public void limparFormulario(){
        txtNomeP.setText("");
        txtCategoria.setText("");
        txtPreco.setText("");
        txtQnt.setText("");
        cbCategoria.setSelectedIndex(0);
    }

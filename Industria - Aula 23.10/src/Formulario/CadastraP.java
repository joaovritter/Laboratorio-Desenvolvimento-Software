    private void btSalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvaActionPerformed
       Produto p = new Produto();
        p.setNome(txtNome.getText());
        p.setPreco(Double.parseDouble(txtPreco.getText()));
        p.setQnt(Integer.parseInt(txtQnt.getText()));
        p.getId_categoria((Categoria) cbCategoria.getSelectedItem());
       
        ProdutoDAO pDAO = new ProdutoDAO();
        pDAO.inserir(p);
    }//GEN-LAST:event_btSalvaActionPerformed

    
    public void preencherComboCategorias() {
        CategoriaDAO cDAO = new CategoriaDAO();
        List<Categoria> listac = cDAO.getCategorias();
        for (Categoria c : listac) {
        cbCategoria.addItem(c);
       }
    }

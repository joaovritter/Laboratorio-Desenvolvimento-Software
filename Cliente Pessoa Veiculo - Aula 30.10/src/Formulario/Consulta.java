    private void btn_ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ConsultarActionPerformed
        try {
            // TODO add your handling code here:
            String host = "localhost";
            int porta = 12345;
            
            Socket clienteSocket = new Socket (host,porta);
            
            ObjectOutputStream out = new ObjectOutputStream (clienteSocket.getOutputStream());
            ObjectInputStream in = new ObjectInputStream (clienteSocket.getInputStream());
            
            int id = Integer.parseInt(txt_IdPessoa.getText());
            out.writeInt(id);
            out.flush();
            
            Pessoa p = (Pessoa) in.readObject();
            System.out.println("Resposta: "+ p.getNome());
            lbl_Nome.setText(p.getNome());
            lbl_Sexo.setText(p.getSexo());
            lbl_Idioma.setText(p.getIdioma());
            
            //Fechamento adequado dos fluxos e do socket
            out.close();
            in.close();
            clienteSocket.close();
            
            
        } catch (IOException ex) {
            System.out.println("Erro na comunicacao com o servidor");
        } catch (ClassNotFoundException ex) {
            System.out.println("Classe pessoa nao encontrada");
        }
        
    }//GEN-LAST:event_btn_ConsultarActionPerformed

